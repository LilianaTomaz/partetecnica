/* Você está em uma sala com três interruptores, cada um conectado a uma lâmpada em salas diferentes. Você não pode ver as lâmpadas
da sala em que está, mas pode ligar e desligar os interruptores quantas vezes quiser. Seu objetivo é descobrir qual interruptor
controla qual lâmpada. Como você faria para descobrir, usando apenas duas idas até uma das salas das lâmpadas, qual interruptor
controla cada lâmpada? 

Passo 1: Definir os interruptores: A, B e C. Correspondentes as lâmpadas: L1, L2 e L3
Passo 2: Ligar o interruptor A e esperar alguns minutos e desligar. A lâmpada correspondente ao interruptor A ficará quente.
Ligar o interruptor B. A lâmpada correspondente ao interruptor B ficará ligada.
Passo 3: Ir na primeira sala, se a lâmpada estiver acesa, corresponde ao interruptor B, 
se a lâmpada estiver quente, corresponde ao interruptor A.
se a lâmpada estiver fria e apagada, corresponde ao interruptor C.
Passo 4: Ir na segunda sala, se a lâmpada estiver acesa, corresponde ao interruptor B, 
se a lâmpada estiver quente, corresponde ao interruptor A.
se a lâmpada estiver fria e apagada, corresponde ao interruptor C.

Com isso, em duas idas as salas, descobrimos qual interruptor esta ligada a cada lâmpada.  */